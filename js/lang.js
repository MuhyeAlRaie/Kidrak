// Function to switch language
function switchLanguage(lang) {
  // Update HTML direction
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;
  
  // Store language preference
  localStorage.setItem('kidrakLang', lang);
  
  // Update all elements with data-lang attributes
  document.querySelectorAll('[data-lang]').forEach(element => {
    const key = element.getAttribute('data-lang');
    if (langContent[lang][key]) {
      element.textContent = langContent[lang][key];
    }
  });
  
  // Update placeholders
  document.querySelectorAll('[data-placeholder]').forEach(element => {
    const key = element.getAttribute('data-placeholder');
    if (langContent[lang][key]) {
      element.placeholder = langContent[lang][key];
    }
  });
  
  // Update language switcher button
  const langSwitcher = document.getElementById('langSwitcher');
  if (langSwitcher) {
    langSwitcher.textContent = lang === 'ar' ? 'EN' : 'AR';
  }
}

// Initialize language from localStorage or default to English
document.addEventListener('DOMContentLoaded', function() {
  const savedLang = localStorage.getItem('kidrakLang') || 'ar';
  switchLanguage(savedLang);
  
  // Add event listener to language switcher
  const langSwitcher = document.getElementById('langSwitcher');
  if (langSwitcher) {
    langSwitcher.addEventListener('click', function() {
      const currentLang = document.documentElement.lang;
      const newLang = currentLang === 'ar' ? 'en' : 'ar';
      switchLanguage(newLang);
    });
  }
});